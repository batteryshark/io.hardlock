int dbg_compare(unsigned char* data_1, unsigned char* data_2, unsigned int size) {
    for (int i = 0; i < size; i++) {
        if (data_1[i] != data_2[i]) {
            printf("Mismatch at Offset: %d\n", i);
            return 0;
        }
    }
    return 1;
}

void print_buffer(unsigned char* data, unsigned int len) {
    for (int i = 0; i < len; i++) {
        printf("%02X", data[i]);
    }
    printf("\n\n");
}

